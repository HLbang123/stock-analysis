/** 获取所有板块的股票（去重） */
export function getAllStocks(): { code: string; name: string }[] {
  const seen = new Set<string>();
  const all: { code: string; name: string }[] = [];
  for (const sector of SECTORS) {
    for (const stock of sector.stocks) {
      if (!seen.has(stock.code)) {
        seen.add(stock.code);
        all.push(stock);
      }
    }
  }
  return all;
}
